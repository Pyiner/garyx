function formatTranslation(template: string, params?: TranslationParams): string {
  if (!params) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (match, key) => {
    const value = params[key];
    return value === null || value === undefined ? match : String(value);
  });
}

export function normalizeLanguagePreference(
  value: DesktopLanguagePreference | string | null | undefined,
): DesktopLanguagePreference {
  return value === 'en' || value === 'zh-CN' || value === 'system' ? value : 'system';
}

function localeFromLanguageTag(value: string | null | undefined): AppLocale | null {
  const normalized = value?.trim().toLowerCase();
  if (!normalized) {
    return null;
  }
  if (normalized === 'zh' || normalized.startsWith('zh-')) {
    return CHINESE_LOCALE;
  }
  if (normalized === 'en' || normalized.startsWith('en-')) {
    return ENGLISH_LOCALE;
  }
  return null;
}

function systemLocale(): AppLocale {
  const languages = typeof navigator !== 'undefined'
    ? [navigator.language, ...(navigator.languages || [])]
    : [];
  for (const language of languages) {
    const locale = localeFromLanguageTag(language);
    if (locale) {
      return locale;
    }
  }
  return ENGLISH_LOCALE;
}

export function resolveLocale(preference: DesktopLanguagePreference): AppLocale {
  switch (normalizeLanguagePreference(preference)) {
    case 'en':
      return ENGLISH_LOCALE;
    case 'zh-CN':
      return CHINESE_LOCALE;
    case 'system':
    default:
      return systemLocale();
  }
}

export function createTranslator(locale: AppLocale): Translate {
  return (key, params) => {
    const translated = dictionaries[locale][key] || key;
    return formatTranslation(translated, params);
  };
}

export function useResolvedLocale(preference: DesktopLanguagePreference): AppLocale {
  const normalizedPreference = normalizeLanguagePreference(preference);
  const [locale, setLocale] = useState(() => resolveLocale(normalizedPreference));

  useEffect(() => {
    const update = () => {
      setLocale(resolveLocale(normalizedPreference));
    };
    update();
    window.addEventListener('languagechange', update);
    return () => {
      window.removeEventListener('languagechange', update);
    };
  }, [normalizedPreference]);

  return locale;
}

const I18nContext = createContext<I18nContextValue>({
  locale: ENGLISH_LOCALE,
  languagePreference: 'system',
  t: createTranslator(ENGLISH_LOCALE),
});

export function I18nProvider({
  languagePreference,
  children,
}: {
  languagePreference: DesktopLanguagePreference;
  children: ReactNode;
}) {
  const normalizedPreference = normalizeLanguagePreference(languagePreference);
  const locale = useResolvedLocale(normalizedPreference);
  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      languagePreference: normalizedPreference,
      t: createTranslator(locale),
    }),
    [locale, normalizedPreference],
  );

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  return useContext(I18nContext);
}

export const I18nConsumer = I18nContext.Consumer;

export function isAuthFlowQrCardBoilerplateText(
  text: string,
  locale: AppLocale,
): boolean {
  const normalized = text.trim().toLocaleLowerCase();
  if (!normalized) {
    return false;
  }
  return AUTH_FLOW_QR_CARD_SUPPRESSED_TEXT[locale].some((phrase) => {
    return normalized.includes(phrase.toLocaleLowerCase());
  });
}

export function languagePreferenceLabel(
  preference: DesktopLanguagePreference,
  t: Translate,
): string {
  switch (normalizeLanguagePreference(preference)) {
    case 'en':
      return t('English');
    case 'zh-CN':
      return t('Chinese');
    case 'system':
    default:
      return t('Follow System');
  }
}
